package Model;

/**
 *
 * @author win
 */
public class Menu {
    int mid;
    String mname;
    double price;

    public Menu() {
    }

    public Menu(int mid, String mname, double price) {
        this.mid = mid;
        this.mname = mname;
        this.price = price;
    }

    public int getMid() {
        return mid;
    }

    public void setMid(int mid) {
        this.mid = mid;
    }

    public String getMname() {
        return mname;
    }

    public void setMname(String mname) {
        this.mname = mname;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
    
    
    
}



